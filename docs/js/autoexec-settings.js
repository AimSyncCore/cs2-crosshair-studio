export function defaultAutoexecState() {
  const values = {};
  const enabled = { crosshair: true };

  AUTOEXEC_CATEGORIES.forEach((category) => {
    if (category.id !== "crosshair") {
      enabled[category.id] = category.id === "mouse" || category.id === "viewmodel";
    }
  });

  AUTOEXEC_SETTINGS.forEach((setting) => {
    values[setting.id] = setting.default;
    enabled[setting.id] = enabled[setting.category] ?? false;
  });

  return { values, enabled };
}
